#include <stdio.h>
#include <stdlib.h>

int main()
{
    int num1 = 10;
    printf("integer = %d\n",num1);

    float num2 = 20.5;
    printf("floatnum = %f\n",num2);

    char c = 'a';
    printf("character = %c\n",c);

    int Hexadecimal = 15;
    printf("Hexadecimal = %x\n",Hexadecimal);

    long Longnum = 15;
    printf("Longnum = %ld\n",Longnum);

    double doublenum = 5;
    printf("doublenum = %lf\n",doublenum);


    return 0;
}
